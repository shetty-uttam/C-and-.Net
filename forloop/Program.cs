using System;
class Program
{
    static void Main()
    {

        // Example of a for loop that calculates the sum of numbers from 1 to 10
        int sum = 0;
        for (int j = 1; j <= 10; j++)
        {
            sum += j; // Equivalent to: sum = sum + j;
        }
        Console.WriteLine("Sum of numbers from 1 to 10: " + sum);
            Console.ReadLine();
        }
    }
    
